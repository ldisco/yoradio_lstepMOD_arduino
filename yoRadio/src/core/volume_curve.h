// [Gemini3Pro] Вспомогательная функция для получения значения из таблицы
// Вход: проценты (0-100) или сырое значение (0-255).
// Если вход > 100, он будет обрезан (если считать, что вход - это проценты).
// Но так как у нас volume может быть до 254 (конфиг), нам нужно масштабировать.
inline uint8_t getLogVolume(uint8_t volume255) {
    if (volume255 > 254) volume255 = 254;
    return volumeLogCurve[volume255];
}

